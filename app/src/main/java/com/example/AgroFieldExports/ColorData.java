package com.example.AgroFieldExports;

public class ColorData {
    private int red, green, blue;

    public ColorData() {
        red = 15;
        green = 15;
        blue = 150;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }
}
